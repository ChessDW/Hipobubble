function setLanguage(lang) {
    localStorage.setItem('lang', lang);
    const t = translations[lang] || translations['es'];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            // Si el elemento contiene HTML (como <i>), solo cambia el texto si es puro texto
            if (el.childNodes.length === 1 && el.childNodes[0].nodeType === 3) {
                el.textContent = t[key];
            } else {
                // Si tiene hijos (como <i>), solo cambia el nodo de texto
                el.childNodes.forEach(node => {
                    if (node.nodeType === 3) node.textContent = t[key];
                });
            }
        }
    });
}

function loadLanguage() {
    let lang = localStorage.getItem('lang');
    if (!lang) {
        // Detectar idioma navegador
        const navLang = navigator.language.slice(0,2);
        if (["es","en","fr"].includes(navLang)) lang = navLang; else lang = "es";
    }
    setLanguage(lang);
    // Seleccionar en el select
    const select = document.getElementById('languageSelect');
    if (select) select.value = lang;
}
