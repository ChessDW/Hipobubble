function toggleDarkMode(){
    const body = document.body;
    const isDarkMode = body.classList.toggle('dark-mode');

    localStorage.setItem('darkMode', isDarkMode);

    updateThemeIcon(isDarkMode);
}

function updateThemeIcon(isDarkMode){
    const themeToggle = document.querySelector('.theme-toggle')
}

function loadThemePreference(){
    const savedTheme = localStorage.getItem('darkMode');
    const prefersDark = window.matchMedia(`(prefers-color-scheme: dark)`).matches;

    if(savedTheme !== null){
        if(savedTheme === 'true') {
            document.body.classList.add('dark-mode');
            updateThemeIcon(true);
        }
    } else if (prefersDark){
        document.body.classList.add('dark-mode');
        updateThemeIcon(true);
    }
}

document.addEventListener('DOMContentLoaded', function(){
    window.addEventListener('scroll', updateScrollProgress);
    window.addEventListener('resize', updateScrollProgress);
    updateScrollProgress();

    // Modal de configuración
    const openConfigModal = document.getElementById('openConfigModal');
    const configModal = document.getElementById('configModal');
    const closeConfigModal = document.getElementById('closeConfigModal');
    const toggleDarkModeModal = document.getElementById('toggleDarkModeModal');

    const languageSelect = document.getElementById('languageSelect');

    // Sincronizar checkbox con modo oscuro
    function syncDarkModeCheckbox() {
        toggleDarkModeModal.checked = document.body.classList.contains('dark-mode');
    }

    if (openConfigModal && configModal && closeConfigModal && toggleDarkModeModal) {
        openConfigModal.addEventListener('click', function(e) {
            configModal.classList.add('active');
            syncDarkModeCheckbox();
            loadLanguage();
        });
        closeConfigModal.addEventListener('click', function() {
            configModal.classList.remove('active');
        });
        configModal.addEventListener('click', function(e) {
            if (e.target === configModal) configModal.classList.remove('active');
        });
        toggleDarkModeModal.addEventListener('change', function() {
            toggleDarkMode();
            syncDarkModeCheckbox();
        });
        if (languageSelect) {
            languageSelect.addEventListener('change', function() {
                setLanguage(this.value);
            });
        }
    }

    loadThemePreference();

    loadLanguage();

    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e)=>{
        if(localStorage.getItem('darkMode') === null){
            if(e.matches){
                document.body.classList.add('dark-mode');
                updateThemeIcon(true);
            } else{
                document.body.classList.remove('dark-mode');
                updateThemeIcon(false);
            }
        }
    });

    // Botón volver arriba
    const backToTop = document.getElementById('backToTop');
    if (backToTop) {
        backToTop.addEventListener('click', function() {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });
    }

    // Resaltar sección activa en el menú de navegación
    const sections = document.querySelectorAll('section[id]');
    const navLinks = document.querySelectorAll('.nav-link');
    window.addEventListener('scroll', () => {
        let current = '';
        sections.forEach(section => {
            const sectionTop = section.offsetTop - 120;
            if (window.scrollY >= sectionTop) {
                current = section.getAttribute('id');
            }
        });
        navLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href') === '#' + current) {
                link.classList.add('active');
            }
        });
    });
});